function discountedProduct() {
    if ((discountedProductValues.length != 0 || reviewProducts.length != 0) && list.length != 0) {
        let minD = discountedProductValues.length != 0 ? Math.min(...discountedProductValues) : 0;
        let minR = reviewProducts.length != 0 ? Math.min(...reviewProducts) : 0;

        for (let product of list) {
            let countP = 0;
            let products = document.querySelectorAll(`.p-btn--${product}`);
            for (let selectedProduct of products) {
                if (selectedProduct.getAttribute("value") < minD || selectedProduct.getAttribute("reviewRating") < minR) {
                    selectedProduct.classList.add("product-not-active");
                    countP++;
                } else {
                    selectedProduct.classList.remove("product-not-active");
                }
            }
            if (countP == products.length) {
                document.querySelector(`.data-btn-${product}`).checked = false;
            } else {
                if (document.querySelector(`.data-btn-${product}`).disabled != true) {
                    document.querySelector(`.data-btn-${product}`).checked = true;
                }
            }
        }
    }
}

