function overallDiscountReview() {
    let minD = discountedProductValues.length != 0 ? Math.min(...discountedProductValues) : 0;
    let minR = reviewProducts.length != 0 ? Math.min(...reviewProducts) : 0;

    let allCheckBox = document.querySelectorAll(".AllCheckBox");
    allCheckBox.forEach((element) => element.disabled = true);
    let checkboxValues = document.querySelectorAll(".check");

    for (let product of checkboxValues) {

        if (product.id != "reviewData" && product.id != "discountCheckBox") {
            let countP = 0;
            var ps = document.querySelectorAll(`.p-btn--${product.value}`);
            if (discountedProductValues.length != 0 || reviewProducts.length != 0) {
                for (let selectedProduct of ps) {
                    if ((selectedProduct.getAttribute("value") < minD || selectedProduct.getAttribute("reviewRating") < minR)) {
                        selectedProduct.classList.add("product-active");
                        countP++;
                    } else {
                        selectedProduct.classList.remove("product-active");
                    }
                }
            }
            if (countP == ps.length) {
                document.querySelector(`.data-btn-${product.value}`).checked = false;
                document.querySelector(`.data-btn-${product.value}`).disabled = true;
                checkboxData.splice(checkboxData.indexOf(product.value), 1);
            } else {
                document.querySelector(`.data-btn-${product.value}`).disabled = false;
                document.querySelector(`.data-btn-${product.value}`).checked = true;
                if (!checkboxData.includes(product.value)) {
                    checkboxData.push(product.value);
                }
            }
        }
    }

    if (discountedProductValues.length == 0 && reviewProducts.length == 0) {
        checkboxData.splice(0, checkboxData.length);
        checkboxValues.forEach((checkBox) => {
            document.querySelector(`.data-btn-${checkBox.value}`).checked = false;
            document.querySelector(`.data-btn-${checkBox.value}`).disabled = false;
        });
        allCheckBox.forEach((element) => element.disabled = false);
        product.forEach((productBox) => productBox.classList.remove("product-active"));
    }
    checkboxs();
}

function checkboxs() {
    for (var c of checkboxData) {
        if (!removeProduct.includes(c)) {
            document.querySelector(`.data-btn-${c}`).disabled = false;
            document.querySelector(`.data-btn-${c}`).checked = true;
        }
    }
    if (removeProduct.length != 0) {
        for (let r of removeProduct) {
            let ps = document.querySelectorAll(`.p-btn--${r}`);
            ps.forEach((p) => p.classList.add("product-active"));
            document.querySelector(`.data-btn-${r}`).checked = false;
        }
    }
    if (newAddedProducts.length != 0) {
        let minD = discountedProductValues.length != 0 ? Math.min(...discountedProductValues) : 0;
        let minR = reviewProducts.length != 0 ? Math.min(...reviewProducts) : 0;
        for (let newProducts of newAddedProducts) {
            let product = document.querySelectorAll(`.p-btn--${newProducts}`);
            product.forEach((products) => {
                if ((products.getAttribute("value") < minD || products.getAttribute("reviewRating") < minR)) {
                    products.classList.add("product-active");
                } else {
                    products.classList.remove("product-active");
                    document.querySelector(`.data-btn-${newProducts}`).checked = true;
                }
            });
        }
    }
}

